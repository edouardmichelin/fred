package com.fred.app.data.repository.model

enum class Diet {
    Vegan,           // No animal products at all
    Vegetarian,      // No meat, but may include dairy and eggs
    Pescatarian,     // Vegetarian but includes fish
    Omnivore,        // Eats both plant-based and animal-based foods
    Carnivore,       // Primarily or exclusively eats meat
    Flexitarian,     // Mostly plant-based but occasionally eats meat or fish
    Other
}
